func TestParseNewsFile(t *testing.T) {
	assert := assert.New(t)
	require := require.New(t)

	f, err := os.ReadFile("testdata/news.nix")
	require.NoError(err)

	news, err := parseNewsFile(f)
	assert.NoError(err)
	assert.Len(news, 85)
}
